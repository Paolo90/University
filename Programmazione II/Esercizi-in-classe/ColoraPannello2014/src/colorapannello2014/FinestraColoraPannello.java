    private void BottoneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BottoneActionPerformed
        String s=CampoTesto.getText(); //Action = pressione bottone
        try{
        double n=Double.parseDouble(s);
            if(n>=0){
                Pannello.setBackground(Color.green);
            }else{
                Pannello.setBackground(Color.red);
            }
        }catch(NumberFormatException e){ //guardo la docum di parseDouble()
            Pannello.setBackground(Color.yellow);
        }    
    }//GEN-LAST:event_BottoneActionPerformed

