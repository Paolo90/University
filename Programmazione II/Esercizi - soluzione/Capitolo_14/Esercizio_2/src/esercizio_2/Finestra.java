    private void BottoneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BottoneActionPerformed
        String nome=Testo.getText();
        int lunghezza=nome.length();
        PrintWriter p=null;
        if(!nome.trim().equals("")){
            try{
                p=new PrintWriter(new FileWriter(nome));
            }catch(FileNotFoundException e){
                System.out.println("File "+nome+" non trovato");
                return;
            }catch(IOException e){
                System.out.println("Errore apertura file: "+nome);
                return;
            }
            p.println("Il nome di questo file Ã¨ lungo: "+lunghezza);
            p.close();
            System.exit(0);
        }
    }//GEN-LAST:event_BottoneActionPerformed

